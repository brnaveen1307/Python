# Dictionaries are mutable

dict1 = {1: "Python", 2: "JavaScript", "Name": "Naveen Msd"}
print("Dictionary dict1: ", dict1)

dict1[3] = "Web Development" # Adding new elements: key and value
print("Updated Dictionary: ", dict1)

dict1[2] = "Java" # Updating values with respect to their keys
print("Updated Dictionary: ", dict1)
