tup = (1, 2, 4, 6, 8, 3)
print("Tuple: ", tup)

print("---------------Indexing----------------")

print("tup[3]: ", tup[3])
print("tup[-5]: ", tup[-5])

print("-----------------Slicing----------------")
print("tup[1: 5]: ", tup[1:5])
print("tup[:4]: ", tup[:4])
print("tup[3:]: ", tup[3:])
print("tup[1:6:2]: ", tup[1:6:2])
print("tup[::3]: ", tup[::3])
print("tup[::-1]: ", tup[::-1])
print("tup[-1:1:-1]: ", tup[-1:1:-1])